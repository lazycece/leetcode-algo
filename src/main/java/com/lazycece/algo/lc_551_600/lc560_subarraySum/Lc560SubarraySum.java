package com.lazycece.algo.lc_551_600.lc560_subarraySum;

/**
 * @author lazycece
 * @date 2022/8/21
 */
public class Lc560SubarraySum {

    public int subarraySum(int[] nums, int k) {
        int res = 0;
        for (int i = 0; i < nums.length; i++) {
            int sum = 0;
            for (int j = i; j < nums.length; j++) {
                sum += nums[j];
                if (sum == k) res++;
            }
        }
        return res;
    }
}
