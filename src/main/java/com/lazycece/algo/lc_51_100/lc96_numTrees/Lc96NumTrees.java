package com.lazycece.algo.lc_51_100.lc96_numTrees;

/**
 * @author lazycece
 * @date 2022/7/6
 */
public class Lc96NumTrees {

    public int numTrees(int n) {

        // f(3) = f(0)*f(2) + f(1)*f(1) +  f(2)*f(0)
        int[] dp = new int[n + 1];
        dp[0] = 1;
        for (int i = 1; i <= n; i++) {
            for (int j = 1; j <= i; j++) {
                dp[i] += dp[j - 1] * dp[i - j];
            }
        }
        return dp[n];
    }
}
