package com.lazycece.algo.lc_351_400.lc389_findTheDifference;

/**
 * 位运算
 *
 * @author lazycece
 * @date 2022/8/26
 */
public class Lc389FindTheDifference {

    public char findTheDifference(String s, String t) {
        int res = 0;
        for (int i = 0; i < s.length(); i++) {
            res ^= s.charAt(i);
        }
        for (int i = 0; i < t.length(); i++) {
            res ^= t.charAt(i);
        }
        return (char) res;
    }
}
