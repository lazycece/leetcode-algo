package com.lazycece.algo.lc_101_150.lc142_LinkedListCycleii;

import com.lazycece.model.ListNode;

/**
 * @author lazycece
 * @date 2022/5/4
 */
public class Lc142LinkedListCycleii {

    public ListNode detectCycle(ListNode head) {
        ListNode slow = head;
        ListNode fast = head;
        while (fast != null && fast.next != null) {
            slow = slow.next;
            fast = fast.next.next;
            if (fast == slow) {
                ListNode p1 = fast;
                ListNode p2 = head;
                // 两个指针，从头结点和相遇结点，各走一步，直到相遇，相遇点即为环入口
                while (p1 != p2) {
                    p1 = p1.next;
                    p2 = p2.next;
                }
                return p1;
            }
        }
        return null;
    }

    public static void main(String[] args) {

    }
}
