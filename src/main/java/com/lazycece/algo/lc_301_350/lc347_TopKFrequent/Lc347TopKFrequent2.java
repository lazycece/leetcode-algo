package com.lazycece.algo.lc_301_350.lc347_TopKFrequent;

import com.lazycece.utils.ArrayUtils;

import java.util.*;

/**
 * @author lazycece
 * @date 2022/6/25
 */
public class Lc347TopKFrequent2 {

    public int[] topKFrequent(int[] nums, int k) {
        int[] result = new int[k];
        HashMap<Integer, Integer> map = new HashMap<>();
        for (int num : nums) {
            map.put(num, map.getOrDefault(num, 0) + 1);
        }

        Set<Map.Entry<Integer, Integer>> entries = map.entrySet();
        // 根据map的value值正序排，相当于一个小顶堆
        PriorityQueue<Map.Entry<Integer, Integer>> queue = new PriorityQueue<>((o1, o2) -> o1.getValue() - o2.getValue());
        for (Map.Entry<Integer, Integer> entry : entries) {
            queue.offer(entry);
            if (queue.size() > k) {
                queue.poll();
            }
        }
        for (int i = k - 1; i >= 0; i--) {
            result[i] = queue.poll().getKey();
        }
        return result;
    }

    public static void main(String[] args) {
        Lc347TopKFrequent2 lc = new Lc347TopKFrequent2();
        int[] nums = {1, 1, 1, 2, 2, 2, 3};
        int k = 2;
        ArrayUtils.print(lc.topKFrequent(nums, k));
    }
}
