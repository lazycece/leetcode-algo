/**
 * @author lazycece
 * @date 2022/8/22
 */
public class Lc287FindDuplicate3 {

    public int findDuplicate(int[] nums) {
        //1,3,4,2,2
        //0,1,2,3,4
        int slow = nums[0];
        int fast = nums[nums[0]];
        while (slow != fast) {
            slow = nums[slow];
            fast = nums[nums[fast]];
        }
        slow = 0;
        while (slow != fast) {
            slow = nums[slow];
            fast = nums[fast];
        }
        return slow;
    }
}
